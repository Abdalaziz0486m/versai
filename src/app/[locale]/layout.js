import { notFound } from "next/navigation";
import { NextIntlClientProvider, hasLocale } from "next-intl";
import { routing } from "@/i18n/routing";
import { ThemeProvider } from "@/contexts/ThemeContext";
import BootstrapClient from "@/components/ui/BootstrapConect";
import Navbar from "@/components/layout/navbar/Navbar";
import "@fortawesome/fontawesome-free/css/all.min.css";
import "bootstrap/dist/css/bootstrap.min.css";
import "../styles/globals.css";
import FaviconSwitcher from "@/components/ui/FaviconSwitcher";
import Search from "@/components/layout/Search";
import Footer from "@/components/layout/Footer";
import ProductsSection from "@/components/product/ProductsSection";

export const metadata = {
  title: "Versai",
  description: "Generated by create next app",
  icons: {
    icon: "/favicon-light.png", // fallback favicon
  },
};

export default async function RootLayout({ children, params }) {
  const { locale } = await params;
  if (!hasLocale(routing.locales, locale)) {
    notFound();
  }
  return (
    <html
      lang={locale}
      dir={locale === "ar" ? "rtl" : "ltr"}
      suppressHydrationWarning
    >
      <head>
        <link rel="manifest" href="/manifest.json" />
      </head>
      <body className="" lang={locale} dir={locale === "ar" ? "rtl" : "ltr"}>
        <NextIntlClientProvider>
          <ThemeProvider>
            <BootstrapClient />
            <FaviconSwitcher />
            <Navbar />
            {children}
            <ProductsSection />
            <Footer />
          </ThemeProvider>
        </NextIntlClientProvider>
      </body>
    </html>
  );
}
